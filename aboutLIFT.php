        <h2 class="about-title">Practical</h2>
      </div>
      <p class="about-info">LIFT divides needs and tasks into manageable chores that can be shared by a number of volunteers in a congregation.
        No single individual, pastor, or deacon is left to meet needs alone. Tasks might include respite care, meals, financial or estate planning, housekeeping, yard work, and transportation.
        LIFT has practical ways of enlisting volunteers with different gifts and abilities to serve with minimal management.</p>
    </div>
    <div class="about-containers">
      <div class="title-seperator">
        <h2 class="about-title">Sustainable</h2>
      </div>
      <p class="about-info">Care ministries can be overwhelming because volunteers often lack the time, knowledge, or sufficient direction for exercising their gifts effectively.
        LIFT addresses this problem so the ministry can thrive on its own over the long-haul with the support of a trained team. As Paul said in
        Galatians 6:9: “Let us not become weary in doing good, for at the proper time we will reap a harvest if we do not give up.”</p>
    </div>
    <div class="about-containers">
      <div class="title-seperator">
        <h2 class="about-title">Intentional</h2>
      </div>
      <p class="about-info">Often in our churches, care for the vulnerable is reactive — when a crisis arises or a request is made.
        Many individuals, however, are afraid to ask for help or don’t know what to ask for when they have a need. LIFT is proactive,
        working with family members to develop a structure for care and addressing issues and needs that may not be apparent.
        The goal is to ensure no one is forgotten or feels alone. If we’re not intentional in our care, many will be ignored and feel abandoned.</p>
    </div>
    <div class="about-containers">
      <div class="title-seperator">
        <h2 class="about-title">Educational</h2>
      </div>
      <p class="about-info">The strength of LIFT are the resources and training that develop an effective body of volunteers.
        They learn important concepts of basic care, communication, listening, dealing with people in crisis and suffering, aging and stress,
        the challenges of dementia, bereavement, etc. LIFT also applies biblical principles to issues of death and dying, complicated medical technology,
        and the ethical decisions that families face at the end of a loved one’s life. As these questions are addressed intentionally beforehand,
        a church can play a major role in helping as people suffer, encouraging them to “finish well” for the glory of God.</p>
